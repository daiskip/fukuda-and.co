					<h5>略歴</h5>
					<ul>
						<li>1983年 文学座研究所入所（第23期生）</li>
						<li>1985年 舞台「事ありげな夏の夕暮れ」（西川信廣演出/文学座）でデビュー</li>
						<li>1988年 文学座の座員となり、活動</li>
						<li>2017年 文学座を退団、現在に至る。</li>
					</ul>

					<p>
						文学座時代より劇団内外の舞台のほか、声優としても吹替え、アニメに多数出演。舞台では西瓜糖、羽衣１０１１を主催。「秋之桜子」名義で、脚本家としても活動。小劇場からアニメまで、数多くの脚本を提供している。
